class BookModel {
  final String id;
  final String imagePath;
  final String name;
  final double price;
  final String description;
  final double rating;
  final String genre;

  BookModel({
    required this.id,
    required this.imagePath,
    required this.name,
    required this.price,
    required this.description,
    required this.rating,
    required this.genre,
  });
}
